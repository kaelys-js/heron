#!/usr/bin/env node
/**
 * gen-build-info.mjs -- emit ui/electron/src/build-info.ts (GITIGNORED).
 *
 * The Electron build is reproducible from brand.json + source, but a handful
 * of facts only exist AT BUILD TIME -- the git commit, the moment the build
 * ran, the release channel, and the version's changelog notes (for the About
 * "What's New" + the auto-update card). Baking them into a generated TS module
 * keeps the runtime read synchronous and free of `execSync`/`process.env`
 * scatter, and keeps the file out of git so it never churns the working tree.
 *
 * Mirrors the other scripts/native/*.mjs generators: a small set of PURE
 * helpers (testable, no I/O) plus a thin imperative tail that writes the file
 * when run directly. The package.json electron:make* / electron:pack scripts
 * prepend this so build-info.ts exists before tsgo compiles.
 *
 * Resolution (all overridable so CI and local builds agree):
 *   - commit:   GITHUB_SHA[:7]  ->  `git rev-parse --short HEAD`  ->  'dev'
 *   - buildDate: now (ISO 8601) -- the generator runs at build time
 *   - channel:  HERON_RELEASE_CHANNEL  ->  EP_PRE_RELEASE ? 'beta' : 'stable'
 *   - releaseNotes: the top (current-version) section of CHANGELOG.md, raw md
 */
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';
import { readFileSync, writeFileSync } from 'node:fs';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..', '..');

/**
 * PURE. Extract the top (current-version) release section from a CHANGELOG.md
 * body as a raw markdown string. The changelog is Keep-a-Changelog / Release
 * Please style: an `# Changelog` H1 + preamble, then `## [x.y.z] -- date`
 * sections newest-first. We return everything from the FIRST `## ` heading up
 * to (but excluding) the next `## ` heading, trimmed. Anything above the first
 * `## ` (the title + format blurb) is preamble, not a release, so it's skipped.
 * Returns '' when there is no `## ` section at all.
 */
export function topChangelogSection(changelog) {
  const text = String(changelog ?? '');
  const lines = text.split('\n');
  let start = -1;
  for (let i = 0; i < lines.length; i++) {
    if (/^##\s/.test(lines[i])) {
      start = i;
      break;
    }
  }
  if (start === -1) {
    return '';
  }
  let end = lines.length;
  for (let i = start + 1; i < lines.length; i++) {
    if (/^##\s/.test(lines[i])) {
      end = i;
      break;
    }
  }
  return lines.slice(start, end).join('\n').trim();
}

/**
 * PURE. Resolve the build channel from env-like inputs. An explicit
 * HERON_RELEASE_CHANNEL always wins; otherwise electron-builder's
 * EP_PRE_RELEASE flag (set on pre-release publishes) selects 'beta', and the
 * default is 'stable'. `env` is injectable for tests.
 */
export function resolveChannel(env = process.env) {
  const explicit = String(env.HERON_RELEASE_CHANNEL ?? '').trim();
  if (explicit) {
    return explicit;
  }
  return env.EP_PRE_RELEASE ? 'beta' : 'stable';
}

/**
 * PURE. Resolve the short commit from env + a git-lookup callback. GITHUB_SHA
 * (set in Actions) wins, sliced to 7; otherwise the injected gitShort() result
 * (a `git rev-parse --short HEAD`, which the caller wraps in try/catch); else
 * 'dev'. `gitShort` returning '' / throwing both fall back to 'dev'.
 */
export function resolveCommit(env = process.env, gitShort = () => '') {
  const sha = String(env.GITHUB_SHA ?? '').trim();
  if (sha) {
    return sha.slice(0, 7);
  }
  let short = '';
  try {
    short = String(gitShort() ?? '').trim();
  } catch {
    short = '';
  }
  return short || 'dev';
}

/** Read CHANGELOG.md's top section, or '' if the file is absent/unreadable. */
function readReleaseNotes(root) {
  try {
    return topChangelogSection(readFileSync(join(root, 'CHANGELOG.md'), 'utf8'));
  } catch {
    return '';
  }
}

/** Short HEAD commit via git, or '' when git is absent / not a clone. */
function gitShortHead(root) {
  return execSync('git rev-parse --short HEAD', {
    cwd: root,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'ignore'],
  });
}

/** Assemble the BUILD_INFO record from the live environment + repo. */
export function buildInfo(root = REPO_ROOT, env = process.env) {
  return {
    commit: resolveCommit(env, () => gitShortHead(root)),
    buildDate: new Date().toISOString(),
    channel: resolveChannel(env),
    releaseNotes: readReleaseNotes(root),
  };
}

/** Render the generated build-info.ts source. */
export function renderBuildInfoTs(info) {
  return [
    `// AUTO-GENERATED by scripts/native/gen-build-info.mjs -- do not edit.`,
    `// Regenerated on every desktop build (gitignored; never committed).`,
    ``,
    `/** Build-time facts the About + auto-update windows surface. */`,
    `export type BuildInfo = {`,
    `  /** Short git SHA, or 'dev' for an untracked local build. */`,
    `  commit: string;`,
    `  /** ISO 8601 timestamp of when this build was generated. */`,
    `  buildDate: string;`,
    `  /** Release channel: 'stable' | 'beta' (or an explicit override). */`,
    `  channel: string;`,
    `  /** Current version's CHANGELOG.md section, raw markdown ('' if absent). */`,
    `  releaseNotes: string;`,
    `};`,
    ``,
    `export const BUILD_INFO: BuildInfo = ${JSON.stringify(info, null, 2)} as const;`,
    ``,
  ].join('\n');
}

const OUT = join(REPO_ROOT, 'ui', 'electron', 'src', 'build-info.ts');

// Run directly -> write the file. Imported (tests) -> just export the helpers.
if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  writeFileSync(OUT, renderBuildInfoTs(buildInfo()));
}
